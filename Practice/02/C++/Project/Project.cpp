#include <iostream>

using namespace std;

int main() {
	int a = 3, b = 3.14;
	double c = 3, d = 3.14;

	cout << "int a = " << a << endl << "int b = " << b << endl;
	cout << "double c = " << c << endl << "double d = " << d;

	return 0;
}

// Переменная типа int хранит только целочисленные значения
// Переменная типа double хранит числа с плавающей точкой